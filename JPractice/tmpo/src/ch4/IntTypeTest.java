package ch4;

public class IntTypeTest {

	public static void main(String[] args) {
		int i1 = 1234;
		int i2 = 01230;
		int i3 = 0xFFF;
		
		System.out.println("i1 is " + i1 + "\ni2 is " + i2 + "\ni3 is " + i3);
		
		long l1 = 13L;
		long l2 = 13;
		
		System.out.print("l1 is " + l1 + "\nl2 is " + l2);
		
		int i4 = 13;
		byte b = 40;
		System.out.println("\nResult is " + i4*b);

	}

}
